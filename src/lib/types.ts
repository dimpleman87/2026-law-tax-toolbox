// ============================================================
// 広告配置に関する型
// ============================================================

/** 広告スロットの配置位置（英語識別子） */
export type 広告配置位置型 = "top" | "middle" | "bottom" | "sidebar";

// ============================================================
// ツールのロジック種別に関する型
// ============================================================

/**
 * ツールのUIレンダリング・ロジックを決定する種別。
 */
export type ロジック種別型 =
  | "text-analysis"   // テキスト分析（文字数カウント等）
  | "calculation"     // 数値計算（BMI・消費税等）
  | "date-calculation"// 日付計算（年齢計算等）
  | "conversion"      // 変換ツール（単位変換等）
  | "generator"       // 生成ツール（パスワード生成等）
  | "dog-age";        // 犬の年齢換算（PetAdBanner付き専用UI）

// ============================================================
// 入力フィールドに関する型
// ============================================================

/** 入力フィールドの種別 */
export type 入力種別型 = 
  | "textarea" 
  | "number" 
  | "date" 
  | "text" 
  | "select";

/** 入力フィールドの詳細定義 */
export interface 入力フィールド型 {
  /** フィールドの種別 */
  種別: 入力種別型;
  /** UI上に表示するラベル */
  ラベル: string;
  /** プレースホルダー */
  プレースホルダー?: string;
  /** 単位（例：cm, kg, 円） */
  単位?: string;
  /** 最小値 */
  最小値?: number;
  /** 最大値 */
  最大値?: number;
  /** セレクトの選択肢 */
  選択肢?: string[];
}

// ============================================================
// ツール定義型（メイン）
// ============================================================

/**
 * src/tools/*.json の構造を定義するインターフェース。
 */
export interface ツール定義型 {
  /** 一意のスラッグ（URLの一部） */
  スラッグ: string;
  /** ページのタイトル（SEO用） */
  タイトル: string;
  /** メタディスクリプション */
  説明: string;
  /** キーワード配列 */
  キーワード: string[];
  /** カテゴリ名 */
  カテゴリ: string;
  /** ロジック種別（英語識別子） */
  ロジック種別: ロジック種別型;
  /** 入力項目の定義 */
  入力フィールド: 入力フィールド型[];
  /** 結果として表示する項目名の配列 */
  出力項目: string[];
  /** 広告を配置する位置 */
  広告配置: 広告配置位置型[];
  /** 関連ツールのスラッグ配列 */
  関連ツール?: string[];
  /** SEO用の解説本文（Markdown想定） */
  本文?: string;
  /** 計算結果に基づくAIアドバイスのテンプレート */
  アドバイス?: string;
  /** カテゴリの英語識別子（内部管理用） */
  category?: string;
  /** SNSシェア用投稿テンプレート（{result}プレースホルダーを含む） */
  socialPostTemplates?: string[];
}

// ============================================================
// コンポーネント用の型定義
