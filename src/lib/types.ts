/** AdSlotコンポーネント用 */
export interface 広告スロットProps {
  位置: 広告配置位置型;
  スロットID?: string;
  className?: string;
}

/** ツールカードコンポーネント用 */
export interface ツールカードProps {
  ツール: ツール定義型;
}
